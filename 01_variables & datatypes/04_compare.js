//Diffrence between == and ===

console.log(` Output of "10" === 10 ? - ${"10" === 10}`);
console.log(` Output of "10" == 10 ? - ${"10" == 10}`);

console.log(` Output of "0" === false ? - ${"0" === false}`);
console.log(` Output of "0" == false ? - ${"0" == false}`);

console.log(` Output of null === undefined ? - ${null === undefined}`);
console.log(` Output of null == undefined ? - ${null == undefined}`);