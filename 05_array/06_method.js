//Using methods - push,pop,unshift,shift

//push
let arr_1 = ["apple","banana","mango"];
console.log(`orginal array before push - [${arr_1}]`);
arr_1.push("grapes");
console.log(`orginal array after push - [${arr_1}]\n`);

//pop
let arr_2 = ["apple","banana","mango"];
console.log(`orginal array before pop - [${arr_2}]`);
arr_2.pop();
console.log(`orginal array after pop - [${arr_2}]\n`);

//unshift
let arr_3 = ["apple","banana","mango"];
console.log(`orginal array before unshift - [${arr_3}]`);
arr_3.unshift("grapes");
console.log(`orginal array after unshift - [${arr_3}]\n`);

//shift
let arr_4 = ["apple","banana","mango"];
console.log(`orginal array before shift - [${arr_4}]`);
arr_4.shift();
console.log(`orginal array after shift - [${arr_4}]\n`);