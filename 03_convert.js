//Conversion 

let variable = "113";
console.log(`Value of variable is ${variable}`)
console.log(`Type of variable is ${typeof(variable)}`);


let num = Number(variable);
console.log(`Value of num is ${num}`)
console.log(`Type of num is ${typeof(num)}`);


let bool = Boolean(variable);
console.log(`Value of bool is ${bool}`)
console.log(`Type of bool is ${typeof(bool)}`);

